package BasicsIntro;
public class operators {
    public static void main(String[] args) {
        //arithmetic operators
        int a= 10;
        int b = 20;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a%b);
        System.out.println(b%a);//remainder
        System.out.println(a/b); //quotient
    }
}
